class Candidato:

    def __init__(self, codigo = str,
                 chapa = str,
                 nome = str,
                 cargo = str):
        self.codigo = codigo
        self.chapa = chapa
        self.nome = nome
        self.cargo = cargo


    @property
    def codigo(self):
        pass


    @codigo.setter
    def codigo(codigo):
        pass


    @property
    def nome(self):
        pass


    @codigo.setter
    def nome(nome):
        pass


    @property
    def cargo(self):
        pass


    @cargo.setter
    def cargo(cargo):
        pass

